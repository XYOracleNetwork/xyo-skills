#!/usr/bin/env node
// Mirrors the compiled scaffold output into plugins/xl1-skills/bin/ so the
// Claude Code plugin ships a ready-to-run binary. Claude Code puts the
// plugin's bin/ on PATH, so `bootstrap-xl1-dapp` is callable by name from any
// skill invocation.
//
// Runs as part of `pnpm build` in packages/xl1-scaffold. CI verifies the
// plugin tree is in sync via `git diff --exit-code plugins/xl1-skills/bin`.

import { chmodSync, cpSync, existsSync, mkdirSync, readdirSync, renameSync, rmSync, writeFileSync } from 'node:fs'
import { dirname, join, relative, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const HERE = dirname(fileURLToPath(import.meta.url))
const SCAFFOLD_ROOT = resolve(HERE, '..')
const SRC = resolve(SCAFFOLD_ROOT, 'dist')

// Walk up from the scaffold package to find the workspace root.
function findWorkspaceRoot(startDir) {
  let dir = startDir
  while (dir !== dirname(dir)) {
    if (existsSync(join(dir, 'pnpm-workspace.yaml'))) return dir
    dir = dirname(dir)
  }
  throw new Error(`pnpm-workspace.yaml not found walking up from ${startDir}`)
}

const WORKSPACE_ROOT = findWorkspaceRoot(SCAFFOLD_ROOT)
const DEST = resolve(WORKSPACE_ROOT, 'plugins/xl1-skills/bin')

if (!existsSync(SRC)) {
  console.error(`Source dir missing: ${SRC}. Run tsc first.`)
  process.exit(1)
}

// Clean slate — prevents stale files from lingering when scaffold src is renamed or deleted.
if (existsSync(DEST)) rmSync(DEST, { recursive: true, force: true })
mkdirSync(DEST, { recursive: true })

// Copy filter: keep .js files (runtime), templates dir (raw files the CLI copies),
// drop .d.ts, .d.ts.map, .js.map (not needed at runtime, just diff noise).
const SKIP_SUFFIXES = ['.d.ts', '.d.ts.map', '.js.map']

function copyFiltered(src, dest) {
  const entries = readdirSync(src, { withFileTypes: true })
  for (const entry of entries) {
    const srcPath = join(src, entry.name)
    const destPath = join(dest, entry.name)
    if (entry.isDirectory()) {
      // templates/ is copied wholesale — no filter inside
      if (entry.name === 'templates') {
        mkdirSync(destPath, { recursive: true })
        cpSync(srcPath, destPath, { recursive: true })
        continue
      }
      mkdirSync(destPath, { recursive: true })
      copyFiltered(srcPath, destPath)
      continue
    }
    if (SKIP_SUFFIXES.some(suffix => entry.name.endsWith(suffix))) continue
    cpSync(srcPath, destPath)
  }
}

copyFiltered(SRC, DEST)

// Rename entry to drop the .js extension. Claude Code's plugin bin/ mechanism
// expects bare command names (e.g. 'bootstrap-xl1-dapp'), matching the
// `my-tool`-style example in the docs. Node still loads it as ESM via the
// workspace-root "type": "module" + the shebang.
renameSync(join(DEST, 'bootstrap-xl1-dapp.js'), join(DEST, 'bootstrap-xl1-dapp'))

// Mark the CLI entry executable so it can be invoked as a bare command when
// Claude Code adds this bin/ to PATH. tsc preserves the shebang but not the
// exec bit. Git tracks this mode (mode 0o755) once the file is staged.
chmodSync(join(DEST, 'bootstrap-xl1-dapp'), 0o755)

// Re-emit README so it survives the rmSync above.
writeFileSync(
  join(DEST, 'README.md'),
  `# xl1-skills/bin/ — generated

Do not hand-edit. This directory is regenerated by:

\`\`\`shell
pnpm -w run build
\`\`\`

Source of truth: [\`packages/xl1-scaffold/\`](../../../packages/xl1-scaffold/).

CI enforces sync via \`git diff --exit-code plugins/xl1-skills/bin\`.
`,
)

console.log(`Synced scaffold → ${relative(WORKSPACE_ROOT, DEST)}`)
